/**
 * Pane layout catalog.
 *
 * Each layout describes a CSS Grid configuration plus the placement of N panes.
 * `cells` index in array == pane index (pane 0 is first cell, pane 1 is second, …).
 * Coordinates use CSS Grid `grid-row` / `grid-column` semantics — 1-based.
 *
 * The picker UI renders a small SVG preview for every layout straight from this data,
 * so adding a new layout is a single object entry.
 */

export interface PaneCellRect {
  /** 1-based grid column start. */
  col: number;
  /** 1-based grid row start. */
  row: number;
  /** Number of columns this pane spans. */
  colSpan: number;
  /** Number of rows this pane spans. */
  rowSpan: number;
}

export interface PaneLayout {
  id: string;
  paneCount: number;
  cols: number;
  rows: number;
  cells: PaneCellRect[];
}

const L = (
  id: string,
  cols: number,
  rows: number,
  cells: Array<[number, number, number, number]>,
): PaneLayout => ({
  id,
  paneCount: cells.length,
  cols,
  rows,
  cells: cells.map(([col, row, colSpan, rowSpan]) => ({ col, row, colSpan, rowSpan })),
});

