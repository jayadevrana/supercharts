/** Group layouts by pane count for the picker UI. */
export function groupLayoutsByCount(): Array<{ count: number; layouts: PaneLayout[] }> {
  const map = new Map<number, PaneLayout[]>();
  for (const l of PANE_LAYOUTS) {
    const arr = map.get(l.paneCount) ?? [];
    arr.push(l);
    map.set(l.paneCount, arr);
  }
  return [...map.entries()].sort((a, b) => a[0] - b[0]).map(([count, layouts]) => ({ count, layouts }));
}

export function getLayout(id: string): PaneLayout {
  return PANE_LAYOUTS.find((l) => l.id === id) ?? PANE_LAYOUTS[0]!;
}

/** Default layout id used for fresh terminals. */
